def _extract_function_docs(source: str, names: list[str]) -> list[FunctionDoc]:
    """Extract function signatures and docstrings from source for named functions.

    Args:
        source: Python source code string.
        names: List of function names to extract.

    Returns:
        List of FunctionDoc for each found function.
    """
    tree = ast.parse(source)
    results: list[FunctionDoc] = []

    for node in ast.walk(tree):
        if isinstance(node, (ast.FunctionDef, ast.AsyncFunctionDef)) and node.name in names:
            docstring = ast.get_docstring(node) or ""
            # Build signature from AST
            sig = _build_signature(node)
            results.append(
                FunctionDoc(
                    name=node.name,
                    signature=sig,
                    docstring=docstring,
                )
            )

    return results


def _build_signature(node: ast.FunctionDef | ast.AsyncFunctionDef) -> str:
    """Build a human-readable function signature from an AST FunctionDef."""
    args = node.args
    parts: list[str] = []

    # Regular positional args
    num_defaults = len(args.defaults)
    num_args = len(args.args)
    non_default_count = num_args - num_defaults

    for i, arg in enumerate(args.args):
        if arg.arg == "self" or arg.arg == "cls":
            continue
        annotation = f": {ast.unparse(arg.annotation)}" if arg.annotation else ""
        if i >= non_default_count:
            default_val = ast.unparse(args.defaults[i - non_default_count])
            parts.append(f"{arg.arg}{annotation} = {default_val}")
        else:
            parts.append(f"{arg.arg}{annotation}")

    # Return annotation
    ret = ""
    if node.returns:
        ret = f" -> {ast.unparse(node.returns)}"

    return f"{node.name}({', '.join(parts)}){ret}"


