def _extract_class_docs(source: str, names: list[str]) -> dict[str, str]:
    """Extract class docstrings from source for named classes.

    Args:
        source: Python source code string.
        names: List of class names to extract.

    Returns:
        Mapping of class name to its docstring (first paragraph only).
    """
    tree = ast.parse(source)
    result: dict[str, str] = {}

    for node in ast.walk(tree):
        if isinstance(node, ast.ClassDef) and node.name in names:
            doc = ast.get_docstring(node) or ""
            # Take first paragraph
            if doc:
                first_para = doc.split("\n\n")[0].strip().replace("\n", " ")
                result[node.name] = first_para
            else:
                result[node.name] = ""

    return result


