def _parse_public_api(source: str) -> list[ApiSymbol]:
    """Parse __init__.py to extract __all__ symbols with their group headers.

    Reads the comment headers above each import block to categorise symbols
    into groups (e.g. "Statement processing", "Errors", etc.).
    """
    tree = ast.parse(source)
    lines = source.splitlines()

    # Step 1: Build a map of symbol → import source module
    import_map: dict[str, str] = {}
    for node in ast.walk(tree):
        if isinstance(node, ast.ImportFrom) and node.module:
            for alias in node.names:
                real_name = alias.asname or alias.name
                import_map[real_name] = node.module
        elif isinstance(node, ast.Import):
            for alias in node.names:
                real_name = alias.asname or alias.name
                import_map[real_name] = alias.name

    # Step 2: Parse __all__ list with group comments
    all_node = None
    for node in ast.walk(tree):
        if isinstance(node, ast.Assign):
            for target in node.targets:
                if isinstance(target, ast.Name) and target.id == "__all__":
                    all_node = node.value
                    break

    if not isinstance(all_node, ast.List):
        return []

    # We need the source lines for __all__ to read the # comments
    # Find the line range of __all__
    all_start = all_node.lineno
    all_end = all_node.end_lineno or all_node.lineno

    symbols: list[ApiSymbol] = []
    current_group = "Ungrouped"

    for lineno in range(all_start, all_end + 1):
        line = lines[lineno - 1].strip()

        # Group comment: "# Statement processing"
        comment_match = re.match(r"#\s*(.+)", line)
        if comment_match and not line.startswith("#!"):
            current_group = comment_match.group(1).strip()
            continue

        # Symbol string: '"StatementBatch",' or '"__version__"'
        symbol_match = re.match(r'"(\w+)"', line)
        if symbol_match:
            sym_name = symbol_match.group(1)
            import_source = import_map.get(sym_name, "")

            symbols.append(
                ApiSymbol(
                    name=sym_name,
                    group=current_group,
                    import_source=import_source,
                    docstring="",  # filled in later by the generator
                    kind="",  # filled in later
                )
            )

    return symbols


