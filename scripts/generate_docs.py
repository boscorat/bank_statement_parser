def _parse_cli_subcommands(source: str) -> list[SubcommandInfo]:
    """Parse cli.py AST to extract subcommand definitions from argparse calls.

    Walks the AST looking for ``add_parser()`` calls and their subsequent
    ``add_argument()`` calls, extracting all metadata needed to build a
    CLI reference page.
    """
    tree = ast.parse(source)
    subcommands: list[SubcommandInfo] = []

    # Find the main() function
    main_func = None
    for node in ast.walk(tree):
        if isinstance(node, ast.FunctionDef) and node.name == "main":
            main_func = node
            break

    if main_func is None:
        return subcommands

    # Walk through main() body, tracking variable assignments to subparser names
    # We look for: var = subparsers.add_parser("name", ...)
    # Then: var.add_argument(...)
    subparser_vars: dict[str, SubcommandInfo] = {}

    for stmt in ast.walk(main_func):
        # Detect: var = subparsers.add_parser("name", help=..., description=...)
        if isinstance(stmt, ast.Assign) and len(stmt.targets) == 1 and isinstance(stmt.targets[0], ast.Name):
            var_name = stmt.targets[0].id
            call = stmt.value
            if isinstance(call, ast.Call) and isinstance(call.func, ast.Attribute) and call.func.attr == "add_parser":
                # Extract subcommand name (first positional arg)
                cmd_name = ""
                if call.args and isinstance(call.args[0], ast.Constant):
                    cmd_name = str(call.args[0].value)

                help_text = _get_keyword_str(call, "help") or ""
                description = _get_keyword_str(call, "description") or ""

                info = SubcommandInfo(
                    name=cmd_name,
                    help_text=help_text,
                    description=description,
                    arguments=[],
                )
                subparser_vars[var_name] = info
                subcommands.append(info)

    # Now find all add_argument calls
    for stmt in ast.walk(main_func):
        if (
            isinstance(stmt, ast.Expr)
            and isinstance(stmt.value, ast.Call)
            and isinstance(stmt.value.func, ast.Attribute)
            and stmt.value.func.attr == "add_argument"
            and isinstance(stmt.value.func.value, ast.Name)
        ):
            var_name = stmt.value.func.value.id
            if var_name not in subparser_vars:
                continue

            call = stmt.value
            arg_info = _parse_add_argument(call)
            if arg_info:
                subparser_vars[var_name].arguments.append(arg_info)

    return subcommands


def _parse_add_argument(call: ast.Call) -> ArgInfo | None:
    """Extract an ArgInfo from a single add_argument() call AST node."""
    if not call.args:
        return None

    # Determine the argument name(s)
    names: list[str] = []
    for arg in call.args:
        if isinstance(arg, ast.Constant) and isinstance(arg.value, str):
            names.append(arg.value)

    if not names:
        return None

    is_positional = not names[0].startswith("-")
    # Use the longest --name as the display name
    name = max(names, key=len) if not is_positional else names[0]

    help_text = _get_keyword_str(call, "help") or ""
    action = _get_keyword_str(call, "action")
    metavar = _get_keyword_str(call, "metavar")
    dest = _get_keyword_str(call, "dest") or name.lstrip("-").replace("-", "_")

    # Default value
    default = None
    for kw in call.keywords:
        if kw.arg == "default":
            if isinstance(kw.value, ast.Constant):
                default = repr(kw.value.value)
            elif isinstance(kw.value, ast.Name):
                default = kw.value.id
            break

    # Choices
    choices = None
    for kw in call.keywords:
        if kw.arg == "choices":
            if isinstance(kw.value, ast.List):
                choices = []
                for elt in kw.value.elts:
                    if isinstance(elt, ast.Constant):
                        choices.append(str(elt.value))
            break

    return ArgInfo(
        name=name,
        dest=dest,
        help_text=help_text,
        default=default,
        choices=choices,
        action=action,
        metavar=metavar,
        is_positional=is_positional,
    )


def _get_keyword_str(call: ast.Call, keyword: str) -> str | None:
    """Extract a string-valued keyword argument from an AST Call node.

    Handles both simple string constants and parenthesised string
    concatenations (implicit joins across lines).
    """
    for kw in call.keywords:
        if kw.arg == keyword:
            if isinstance(kw.value, ast.Constant) and isinstance(kw.value.value, str):
                return kw.value.value
            # Handle joined strings: ("foo " "bar") → JoinedStr or implicit concat
            if isinstance(kw.value, ast.JoinedStr):
                parts = []
                for v in kw.value.values:
                    if isinstance(v, ast.Constant):
                        parts.append(str(v.value))
                return "".join(parts)
    return None


