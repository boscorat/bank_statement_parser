def generate_python_api() -> str:
    """Generate the Python API Reference page from __init__.py."""
    init_source = _INIT_PY.read_text(encoding="utf-8")
    symbols = _parse_public_api(init_source)

    # Read source files to get docstrings for each symbol
    source_cache: dict[str, str] = {}
    for sym in symbols:
        if sym.import_source and sym.import_source not in source_cache:
            # Convert module path to file path — try .py first, then __init__.py
            module_path = sym.import_source.replace(".", "/") + ".py"
            full_path = _REPO_ROOT / "src" / module_path
            if full_path.exists():
                source_cache[sym.import_source] = full_path.read_text(encoding="utf-8")
            else:
                # Try as a package (__init__.py)
                init_path = _REPO_ROOT / "src" / sym.import_source.replace(".", "/") / "__init__.py"
                if init_path.exists():
                    source_cache[sym.import_source] = init_path.read_text(encoding="utf-8")

    # Classify and get docstrings
    for sym in symbols:
        # Constants don't need source lookup
        if sym.name.startswith("__"):
            sym.kind = "constant"
            continue

        # Module-level imports (import ... as name) are modules, not functions
        if sym.name in ("db",):
            sym.kind = "module"
            module_source = source_cache.get(sym.import_source, "")
            if module_source:
                mod_doc = _extract_module_docstring(module_source)
                if mod_doc:
                    sym.docstring = mod_doc.split("\n\n")[0].strip().replace("\n", " ")
            continue

        source = source_cache.get(sym.import_source, "")
        if not source:
            sym.kind = "function"  # fallback for missing sources
            continue

        tree = ast.parse(source)
        found = False
        for node in ast.walk(tree):
            if isinstance(node, ast.ClassDef) and node.name == sym.name:
                sym.kind = "class"
                node_doc = ast.get_docstring(node) or ""
                if node_doc:
                    sym.docstring = node_doc.split("\n\n")[0].strip().replace("\n", " ")
                found = True
                break
            if isinstance(node, (ast.FunctionDef, ast.AsyncFunctionDef)) and node.name == sym.name:
                sym.kind = "function"
                node_doc = ast.get_docstring(node) or ""
                if node_doc:
                    sym.docstring = node_doc.split("\n\n")[0].strip().replace("\n", " ")
                found = True
                break

        # If not found directly, follow re-exports (e.g. from sub.mod import name)
        if not found:
            for node in ast.walk(tree):
                if isinstance(node, ast.ImportFrom) and node.module:
                    for alias in node.names:
                        real_name = alias.asname or alias.name
                        if real_name == sym.name:
                            # Read the actual source module
                            reexport_path = _REPO_ROOT / "src" / (node.module.replace(".", "/") + ".py")
                            if reexport_path.exists():
                                reexport_source = reexport_path.read_text(encoding="utf-8")
                                orig_name = alias.name  # the original name in the source module
                                for rnode in ast.walk(ast.parse(reexport_source)):
                                    if isinstance(rnode, ast.ClassDef) and rnode.name == orig_name:
                                        sym.kind = "class"
                                        rdoc = ast.get_docstring(rnode) or ""
                                        if rdoc:
                                            sym.docstring = rdoc.split("\n\n")[0].strip().replace("\n", " ")
                                        found = True
                                        break
                                    if isinstance(rnode, (ast.FunctionDef, ast.AsyncFunctionDef)) and rnode.name == orig_name:
                                        sym.kind = "function"
                                        rdoc = ast.get_docstring(rnode) or ""
                                        if rdoc:
                                            sym.docstring = rdoc.split("\n\n")[0].strip().replace("\n", " ")
                                        found = True
                                        break
                            break

        if not found:
            sym.kind = "function"  # fallback

    doc: list[str] = []

    def w(text: str = "") -> None:
        doc.append(text)

    w(_DO_NOT_EDIT)
    w(_REGENERATE)
    w()
    w("# Python API Reference")
    w()
    w("All public symbols are available from the top-level package:")
    w()
    w("```python")
    w("import bank_statement_parser as bsp")
    w("```")
    w()

