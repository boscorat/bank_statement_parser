@dataclass(slots=True)
class FieldInfo:
    """One field extracted from a dataclass in data.py."""

    name: str
    type_annotation: str
    status: str  # "ACTIVE" or "STUB"
    description: str  # cleaned-up inline comment text


@dataclass(slots=True)
class ClassInfo:
    """One dataclass extracted from data.py."""

    name: str
    docstring: str
    fields: list[FieldInfo]


@dataclass(slots=True)
class ArgInfo:
    """One CLI argument parsed from argparse calls in cli.py."""

    name: str  # e.g. "--pdfs" or "target"
    dest: str  # Python attribute name
    help_text: str
    default: str | None
    choices: list[str] | None
    action: str | None
    metavar: str | None
    is_positional: bool


@dataclass(slots=True)
class SubcommandInfo:
    """One CLI subcommand parsed from cli.py."""

    name: str
    help_text: str
    description: str
    arguments: list[ArgInfo]


@dataclass(slots=True)
class ApiSymbol:
    """One public symbol from __init__.py __all__."""

    name: str
    group: str  # comment header above the import
    import_source: str  # e.g. "bank_statement_parser.modules.statements"
    docstring: str  # first paragraph of the object's docstring
    kind: str  # "class", "function", "module", "constant"


@dataclass(slots=True)
class FunctionDoc:
    """Extracted documentation for a public function."""

    name: str
    signature: str
    docstring: str  # full Google-style docstring


