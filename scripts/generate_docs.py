def generate_cli_reference() -> str:
    """Generate the CLI Reference page from cli.py argparse definitions."""
    source = _CLI_PY.read_text(encoding="utf-8")
    subcommands = _parse_cli_subcommands(source)

    doc: list[str] = []

    def w(text: str = "") -> None:
        doc.append(text)

    w(_DO_NOT_EDIT)
    w(_REGENERATE)
    w()
    w("# CLI Reference")
    w()
    w("The `bsp` command-line tool provides subcommands for processing bank statement")
    w("PDFs and anonymising PDF files. Install the package to get the `bsp` entry point:")
    w()
    w("```bash")
    w("pip install uk-bank-statement-parser")
    w("```")
    w()

    for cmd in subcommands:
        w(f"## `bsp {cmd.name}`")
        w()
        if cmd.description:
            # Wrap long description text
            w(cmd.description)
            w()
        elif cmd.help_text:
            w(cmd.help_text)
            w()

        # Usage line
        usage_parts = [f"bsp {cmd.name}"]
        for arg in cmd.arguments:
            if arg.is_positional:
                usage_parts.append(arg.metavar or arg.name.upper())
            elif arg.action == "store_true":
                usage_parts.append(f"[{arg.name}]")
            elif arg.metavar:
                usage_parts.append(f"[{arg.name} {arg.metavar}]")
            elif arg.choices:
                usage_parts.append(f"[{arg.name} {{{','.join(arg.choices)}}}]")
            else:
                usage_parts.append(f"[{arg.name}]")
        w("```")
        w(" ".join(usage_parts))
        w("```")
        w()

        # Arguments table
        has_positional = any(a.is_positional for a in cmd.arguments)
        has_optional = any(not a.is_positional for a in cmd.arguments)

        if has_positional:
            w("### Positional arguments")
            w()
            w("| Argument | Description |")
            w("| --- | --- |")
            for arg in cmd.arguments:
                if arg.is_positional:
                    w(f"| `{arg.metavar or arg.name}` | {arg.help_text} |")
            w()

        if has_optional:
            w("### Options")
            w()
            w("| Option | Default | Description |")
            w("| --- | --- | --- |")
            for arg in cmd.arguments:
                if not arg.is_positional:
                    default_str = arg.default if arg.default is not None else ""
                    if arg.action == "store_true":
                        default_str = "off"
                    # Clean up default display
                    default_str = default_str.replace("None", "auto-detect").replace("'", "`")
                    if arg.choices:
                        choice_str = ", ".join(f"`{c}`" for c in arg.choices)
                        desc = f"{arg.help_text} Choices: {choice_str}."
                    else:
                        desc = arg.help_text
                    w(f"| `{arg.name}` | {default_str} | {desc} |")
            w()

    # Examples section
    w("## Examples")
    w()
    w("### Process all PDFs in a folder")
    w()
    w("```bash")
    w("bsp process --pdfs ~/statements")
    w("```")
    w()
    w("### Process with a specific project folder")
    w()
    w("```bash")
    w("bsp process --pdfs ~/statements --project ~/my_project")
    w("```")
    w()
    w("### Persist to database only, skip export")
    w()
    w("```bash")
    w("bsp process --pdfs ~/statements --data database --no-export")
    w("```")
    w()
    w("### Anonymise a single PDF")
    w()
    w("```bash")
    w("bsp anonymise statement.pdf")
    w("```")
    w()
    w("### Anonymise all PDFs in a folder")
    w()
    w("```bash")
    w("bsp anonymise ~/statements --folder")
    w("```")

    return "\n".join(doc)


