def _field_table(fields: list[FieldInfo]) -> str:
    """Render a list of FieldInfo as a Markdown table."""
    rows = ["| Field | Type | Status | Description |", "| --- | --- | --- | --- |"]
    for f in fields:
        esc_desc = f.description.replace("|", "\\|")
        status_badge = f.status
        rows.append(f"| `{f.name}` | `{f.type_annotation}` | {status_badge} | {esc_desc} |")
    return "\n".join(rows)


def _class_section(ci: ClassInfo, heading_level: int = 3) -> str:
    """Render a ClassInfo as a Markdown section."""
    hashes = "#" * heading_level
    parts = [f"{hashes} `{ci.name}`", ""]
    if ci.docstring:
        # Take just the first paragraph of the docstring
        paragraphs = ci.docstring.split("\n\n")
        first_para = paragraphs[0].strip().replace("\n", " ")
        parts.append(first_para)
        parts.append("")
    if ci.fields:
        parts.append(_field_table(ci.fields))
        parts.append("")
    return "\n".join(parts)


def _docstring_to_markdown(docstring: str) -> str:
    """Convert a Google-style docstring to Markdown.

    Preserves section headers (Args, Returns, Raises, Example) as bold
    headings and indented content as code/text blocks.
    """
    if not docstring:
        return ""

    lines = docstring.splitlines()
    output: list[str] = []
    in_section = False
    section_name = ""

    for line in lines:
        stripped = line.strip()

        # Detect Google-style section headers (also handles RST-style "Example::")
        section_match = re.match(r"^(Args|Returns|Raises|Example|Note|Warning)s?::?\s*$", stripped)
        if section_match:
            section_name = section_match.group(1)
            if section_name == "Example":
                output.append("")
                output.append(f"**{section_name}:**")
                output.append("")
                output.append("```python")
            else:
                output.append("")
                output.append(f"**{section_name}:**")
                output.append("")
            in_section = True
            continue

        if in_section and section_name == "Example":
            if stripped == "" and output and output[-1] == "```python":
                continue  # skip blank line right after opening fence
            output.append(line.rstrip())
            continue

        if in_section and stripped:
            # Detect arg-style lines: "    param_name: description"
            arg_match = re.match(r"^\s{4,}(\w+)\s*(?:\(.*?\))?\s*:\s*(.+)", line)
            if arg_match:
                output.append(f"- `{arg_match.group(1)}` — {arg_match.group(2)}")
                continue
            # Continuation line
            arg_cont_match = re.match(r"^\s{8,}(.+)", line)
            if arg_cont_match:
                output.append(f"  {arg_cont_match.group(1).strip()}")
                continue

        if not stripped and in_section and section_name == "Example":
            output.append("```")
            in_section = False
            section_name = ""
            output.append("")
            continue

        if not stripped:
            in_section = False
            section_name = ""
            output.append("")
            continue

        output.append(stripped)

    # Close any unclosed example block
    if in_section and section_name == "Example":
        output.append("```")

    return "\n".join(output)


